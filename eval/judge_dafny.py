import json
import logging
from typing import Optional

import ollama

logger = logging.getLogger(__name__)

DAFNY_JUDGE_SYSTEM_PROMPT = """\
You are an expert Dafny programmer and formal verification specialist evaluating two Dafny implementations against a structured specification.

You will receive:
1. The original natural-language requirement.
2. The structured schema specification (JSON) that both implementations should satisfy.
3. A reference Dafny implementation (generated from the ground-truth schema).
4. A predicted Dafny implementation (generated by the pipeline).

Your task is to evaluate whether the predicted Dafny implementation correctly and faithfully implements the schema specification, and how it compares to the reference implementation.

## Output Format

You MUST output ONLY a single JSON object. No markdown, no explanations outside the JSON.

```json
{
  "is_correct": <bool>,
  "semantic_equivalence_score": <float between 0.0 and 1.0>,
  "matches_specification": <bool>,
  "compiles_likely": <bool>,
  "would_verify": <bool>,
  "differences": ["<string>"],
  "missing_elements": ["<string>"],
  "extra_elements": ["<string>"],
  "reasoning": "<string>"
}
```

## Scoring Guidelines

- `is_correct`: true if the predicted Dafny correctly implements the specification (has correct method name, parameters, return type, preconditions, postconditions, and body logic).
- `semantic_equivalence_score`: how close is the predicted implementation to the reference in terms of behavior. 1.0 = identical behavior, 0.0 = completely different.
- `matches_specification`: true if all requirements from the schema are reflected in the Dafny code.
- `compiles_likely`: true if the code looks syntactically valid Dafny (has module, method/function, balanced braces, etc.).
- `would_verify`: true if the ensures clauses appear to fully capture the specification and the body appears to satisfy them.

## Evaluation Criteria

1. **Module structure**: Must have a `module` declaration.
2. **Method/function name**: Should match the schema's `function_name` (converted to PascalCase).
3. **Parameters**: Must match the schema's inputs in name, type, and order.
4. **Return type**: Must match the schema's output type.
5. **Requires clauses**: Should include preconditions from assumptions and input constraints.
6. **Ensures clauses**: Should include postconditions from success_condition, observations, and edge cases.
7. **Body**: Should implement the logic described by the constraints and success_condition.
8. **Comments**: Brief doc comment is good but not required.

## Important Notes

- The predicted Dafny may use different variable names, different formatting, or different but equivalent logical expressions. Focus on semantic equivalence.
- If the predicted code uses `function` instead of `method` for a computation that has requires/ensures, that is acceptable if it still verifies.
- If the predicted code omits some ensures clauses but the body still correctly implements the logic, that is acceptable.
- If the predicted code adds extra checks not in the schema, mark them in `extra_elements`.
- If the predicted code misses checks that ARE in the schema, mark them in `missing_elements`.

Be strict but fair. Minor stylistic differences should not lower the score significantly.
"""


def _extract_json(text: str) -> dict:
    text = text.strip()
    if text.startswith("```"):
        lines = text.splitlines()
        if lines and lines[0].startswith("```"):
            lines = lines[1:]
        if lines and lines[-1].strip() == "```":
            lines = lines[:-1]
        text = "\n".join(lines).strip()

    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass

    start = text.find("{")
    end = text.rfind("}") + 1
    if start != -1 and end != -1:
        candidate = text[start:end]
        try:
            return json.loads(candidate)
        except json.JSONDecodeError as exc:
            raise ValueError(
                f"Failed to parse JSON from judge output: {exc}\nRaw: {text}"
            ) from exc

    raise ValueError(f"No JSON object found in judge output:\n{text}")


def judge_dafny(
    nl_requirement: str,
    schema: dict,
    reference_dafny: str,
    predicted_dafny: str,
    model: str = "llama3",
    host: Optional[str] = None,
) -> dict:
    """
    Use an LLM to compare a predicted Dafny implementation against a reference
    implementation and the schema specification.

    Returns a dict with correctness scores, match flags, differences, and reasoning.
    """
    client_kwargs: dict = {}
    if host:
        client_kwargs["host"] = host

    user_prompt = (
        "Evaluate whether the predicted Dafny implementation correctly and faithfully "
        "implements the specification, and how it compares to the reference implementation.\n\n"
        f"## Natural Language Requirement\n{nl_requirement}\n\n"
        f"## Schema Specification\n```json\n{json.dumps(schema, indent=2)}\n```\n\n"
        f"## Reference Dafny (ground truth)\n```dafny\n{reference_dafny}\n```\n\n"
        f"## Predicted Dafny (pipeline output)\n```dafny\n{predicted_dafny}\n```\n\n"
        "Output ONLY the JSON evaluation object. No markdown, no extra text."
    )

    logger.debug("Sending Dafny comparison to judge model=%s", model)

    response = ollama.chat(
        model=model,
        messages=[
            {"role": "system", "content": DAFNY_JUDGE_SYSTEM_PROMPT},
            {"role": "user", "content": user_prompt},
        ],
        **client_kwargs,
    )

    content = response["message"]["content"].strip()
    logger.debug("Raw judge response:\n%s", content)

    return _extract_json(content)
